# 자료형 타입을 공부한다.

#숫자형 자료형
#int형의 정수를 표시가능
print("----숫자형출력")
print(7) #7
print(-5) #-5
#double형의 실수를 표시가능
print(3.1415) #3.1415
#또한 연산식 또한 가능
print(5+8) #13
print(2*9) #18
print(3*(3+2)) #15

#문자형 자료형
print("----문자형출력")
print("문자형 자료형은 이렇게 큰따옴표로 감싸주면 됩니다.")
print('큰따옴표와 작은따옴표의 차이는 특별히 없다.')
print("하지만 사용할때는 큰 따옴표로 시작하면 큰 따옴표로 끝나야하고")
print('작은 따옴표로 시작하면 작은 따옴표로 끝나야한다 혼용은 불가하다.')
